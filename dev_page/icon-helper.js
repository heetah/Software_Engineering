/**
 * Icon Helper - SVG Icon Loader
 * Provides utility functions to load and inject SVG icons
 */

/**
 * Load an SVG icon from the icons directory
 * @param {string} name - Icon name (without .svg extension)
 * @param {string} className - Additional CSS classes for the icon
 * @returns {Promise<string>} HTML string containing the icon
 */
async function loadIcon(name, className = '') {
    try {
        const response = await fetch(`icons/${name}.svg`);
        const svgText = await response.text();
        return `<span class="icon ${className}">${svgText}</span>`;
    } catch (error) {
        console.error(`Failed to load icon: ${name}`, error);
        return '';
    }
}

